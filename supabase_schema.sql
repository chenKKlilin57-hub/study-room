-- Complete Supabase schema for the study room app.
-- Run in Supabase SQL Editor for a fresh project before using the app.

create extension if not exists pgcrypto;

create table if not exists profiles (
  id uuid primary key references auth.users(id) on delete cascade,
  username text,
  last_checkin_date date,
  consecutive_days integer not null default 0,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists study_sessions (
  id bigint generated by default as identity primary key,
  user_id uuid not null references auth.users(id) on delete cascade,
  duration_minutes integer not null check (duration_minutes > 0),
  subject text not null default '未分类',
  started_at timestamptz,
  ended_at timestamptz,
  created_at timestamptz not null default now()
);

create table if not exists tasks (
  id bigint generated by default as identity primary key,
  user_id uuid not null references auth.users(id) on delete cascade,
  task_date date not null,
  text text not null,
  done boolean not null default false,
  duration_minutes integer not null default 0,
  priority text not null default 'medium' check (priority in ('low', 'medium', 'high')),
  parent_id bigint references tasks(id) on delete cascade,
  subtask_count integer not null default 0,
  completed_subtask_count integer not null default 0,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists daily_reviews (
  id bigint generated by default as identity primary key,
  user_id uuid not null references auth.users(id) on delete cascade,
  review_date date not null,
  content text not null default '',
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  unique (user_id, review_date)
);

create table if not exists timer_sessions (
  id uuid primary key default gen_random_uuid(),
  user_id uuid not null references auth.users(id) on delete cascade,
  started_at timestamptz not null,
  duration integer not null,
  remaining integer not null,
  timer_mode text not null,
  is_free_mode boolean not null default false,
  elapsed_in_free_mode integer not null default 0,
  updated_at timestamptz not null default now(),
  created_at timestamptz not null default now()
);

create index if not exists idx_profiles_last_checkin_date on profiles(last_checkin_date);
create index if not exists idx_study_sessions_user_id on study_sessions(user_id);
create index if not exists idx_study_sessions_created_at on study_sessions(created_at desc);
create index if not exists idx_study_sessions_ended_at on study_sessions(ended_at desc);
create index if not exists idx_study_sessions_subject on study_sessions(subject);
create index if not exists idx_tasks_user_date on tasks(user_id, task_date);
create index if not exists idx_tasks_parent_id on tasks(parent_id);
create index if not exists idx_daily_reviews_user_date on daily_reviews(user_id, review_date);
create index if not exists idx_timer_sessions_user_id on timer_sessions(user_id);
create index if not exists idx_timer_sessions_updated_at on timer_sessions(updated_at desc);

alter table profiles enable row level security;
alter table study_sessions enable row level security;
alter table tasks enable row level security;
alter table daily_reviews enable row level security;
alter table timer_sessions enable row level security;

