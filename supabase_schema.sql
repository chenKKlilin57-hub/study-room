create or replace view leaderboard as
select
  p.id as user_id,
  coalesce(p.username, split_part(u.email, '@', 1), '学习者') as username,
  coalesce(sum(s.duration_minutes), 0)::integer as total_minutes,
  count(s.id)::integer as session_count
from profiles p
join auth.users u on u.id = p.id
left join study_sessions s on s.user_id = p.id
group by p.id, p.username, u.email
order by total_minutes desc;

create or replace view daily_leaderboard as
select
  p.id as user_id,
  coalesce(p.username, split_part(u.email, '@', 1), '学习者') as username,
  coalesce(sum(s.duration_minutes), 0)::integer as total_minutes,
  count(s.id)::integer as session_count
from profiles p
join auth.users u on u.id = p.id
left join study_sessions s
  on s.user_id = p.id
  and s.created_at >= date_trunc('day', now() at time zone 'Asia/Shanghai') at time zone 'Asia/Shanghai'
group by p.id, p.username, u.email
order by total_minutes desc;

grant select on leaderboard to authenticated;
grant select on daily_leaderboard to authenticated;

